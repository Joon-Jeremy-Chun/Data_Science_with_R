#1.1Introduction to Machine Learning

#Data comes in the form of features and outcomes.

#We want to build an algorithm that takes feature values as input and returns a prediction when we don’t know the actual outcome. 
#We will train algorithms using datasets for which we do know the actual outcome, and then apply these algorithms in the future to make predictions when we don’t know the actual outcome.
#This is also referred to as supervised learning.

#Supervised learning problems can be divided into those with categorical outcomes, which we refer to as classification; and those with continuous outcomes, which we refer to as prediction.
# X_1, ..., X_p denote the features/predictors/covariates, Y denotes the outcomes, and Y_hat denotes the predictions.